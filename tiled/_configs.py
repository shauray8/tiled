_D64 = {
    "sm_120": [(64, 64), (64, 128)],
    "sm_90":  [(64, 64), (64, 128)],
    "sm_89":  [(64, 64)],
}
_D128 = {
    "sm_120": [(64, 128), (64, 64)],
    "sm_90":  [(64, 128), (64, 64)],
    "sm_89":  [(64, 64)],
}

def get_attn_tile_configs(D: int, sm: str = "sm_120") -> list[tuple[int, int]]:
    return (_D128 if D >= 128 else _D64).get(sm, [(64, 64)])
